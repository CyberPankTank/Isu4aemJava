package Glava_4;

class ElectricGuitar {
    String brand;
    int numOfPickups;
    boolean rockStarUserIt;

    String getBrand() {
        return brand;
    }

    void setBrand(String aBrand) {
        brand = aBrand;
    }

    int getNumOfPickups() {
        return numOfPickups;
    }

    void setNumOfPickups(int num) {
        numOfPickups = num;
    }

    boolean getRockStarUserIt() {
        return rockStarUserIt;
    }

    void setRockStarUserIt(boolean yesOrOn) {
        rockStarUserIt = yesOrOn;
    }
}
